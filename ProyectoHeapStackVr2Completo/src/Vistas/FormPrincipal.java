    public void memoriaUsada(){
        double dataSize = 1024 * 1024;
        TxtAreaPeso.setText(" ");
        Runtime runtime = Runtime.getRuntime();
        TxtAreaPeso.append("Memoria maxima: "+(runtime.maxMemory()/dataSize)/1000+"KB");
        TxtAreaPeso.append("\nMemoria total: " + (runtime.totalMemory() / dataSize)/1000 + "KB");
        TxtAreaPeso.append("\nMemoria libre: " + (runtime.freeMemory() / dataSize)/1000 + "KB");
        TxtAreaPeso.append("\nMemoria usada: " + ((runtime.totalMemory() - runtime.freeMemory()) / dataSize)/1000 + "KB");
    }
    private void BtmArchivoSelectActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BtmArchivoSelectActionPerformed
        //Selecciona el archivo en uso
        String item = (String) ListaArchivo.getSelectedItem();
        LblArchivo.setText(item);
        if("Person".equals((String) ListaArchivo.getSelectedItem())){
            PanelPerson.setSize(400, 220);
            PanelAnimal.setSize(1,1);
        }else{
            if("Animal".equals((String) ListaArchivo.getSelectedItem())){
                PanelAnimal.setSize(400, 200);
                PanelPerson.setSize(1,1);
            }
        }
        memoriaUsada();
    }//GEN-LAST:event_BtmArchivoSelectActionPerformed
