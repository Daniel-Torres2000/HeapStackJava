    private void BtmVaciarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BtmVaciarActionPerformed
        // Limpiamos Heap, Stack, Memoria
        
        if("Animal".equals((String)ListaArchivo.getSelectedItem())){
            a1 = null; //automaticamente vaciamos el vinculo con el objeto
            //por lo tanto
            TxtAreaMemoria.setText(" ");
            if(p1!=null){
                    TxtAreaMemoria.append("\n"+p1.getName()+"\n"+p1.getAge()+"\n"+p1.getSalary());
                }
            TxtAreaStack.append("\na1=null\na1 borrado de stack");
            TxtAreaHeap.append("\nobjeto Animal y atributos borrados de heap");
        }else{
            if("Person".equals((String)ListaArchivo.getSelectedItem())){
                p1 = null; //automaticamente vaciamos el vinculo con el objeto
                //por lo tanto
                TxtAreaMemoria.setText(" ");
                if(a1!=null){
                    TxtAreaMemoria.append("\n"+a1.getSpecies()+"\n"+a1.getName());
                }
                TxtAreaStack.append("\np1=null\np1 borrado de stack");
                TxtAreaHeap.append("\nobjeto Person y atributos borrados de heap");
            }
        }
        memoriaUsada();
    }//GEN-LAST:event_BtmVaciarActionPerformed

